//1. java.lang (Pacote Fundamental)

//  Importado automaticamente, contém classes essenciais para a linguagem. 

//  Object: A classe raiz de toda a hierarquia Java.

//  String: Utilizada para manipular sequências de caracteres imutáveis.

//  System: Fornece acesso a recursos do sistema, como System.out e manipulação de propriedades.

//  Math: Contém métodos para operações matemáticas (seno, cosseno, raiz quadrada, etc.).

//  Classes Wrapper (Integer, Double, Boolean, etc.): Permitem tratar tipos primitivos como objetos.

//  Thread e Runnable: Fundamentais para programação concorrente e multithreading. 


//2. java.util (Coleções e Utilitários)

//  Contém o Framework de Coleções (Collections Framework), estruturas de dados, e classes utilitárias. 

//  ArrayList / LinkedList: Implementações de listas dinâmicas.

//  HashMap / HashSet: Estruturas de dados baseadas em hash (mapas e conjuntos).

//  Collections: Classe utilitária com métodos estáticos para ordenar ou pesquisar coleções.

//  Date / Calendar / LocalDate (Java 8+): Manipulação de datas e horas.

//  Scanner: Utilizada para leitura de dados de entrada. 


//3. java.io e java.nio (Entrada/Saída de Dados) 

//  Classes para leitura e escrita de arquivos e fluxos de dados. 

//  File: Representa caminhos de arquivos e diretórios.

//  InputStream / OutputStream / Reader / Writer: Classes base para leitura e escrita de fluxos de bytes e caracteres. 


//4. java.util.stream (Streams API - Java 8+)

//  Essencial para manipulação funcional de coleções. 

//  Stream: Permite operações de processamento de dados declarativas, como map, filter, e reduce. 


//5. java.sql (Conexão com Banco de Dados) 

//  Connection, Statement, ResultSet: Interfaces principais para executar queries SQL e manipular resultados de bancos de dados. 


//6. java.net (Redes)

//  Socket, ServerSocket, URL: Classes para comunicação via rede e protocolos de internet.